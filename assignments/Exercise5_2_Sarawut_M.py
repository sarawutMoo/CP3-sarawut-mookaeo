# v คืออัตราเร็วของการเคลื่อนที่ (m/s หรือ km/hr)
# t คือเวลาที่วัตถุใช้เคลื่อนที่จริง (s หรือ hr)
# v=s/t
# กรอกระยะทาง โดยมีค่าตั้งแต่ 1 ขึ้นไป ลงในตัวแปร
# กรอกเวลาที่เคลื่อนที่ โดยมีค่าตั้งแต่ 1 ขึ้นไป ลงในตัวแปร
s = int(input('ระยะทางคือ (เมตร) :'))
t = int(input('เวลาที่เคลื่อนที่ (นาที):'))
v = (s/1000)/(t/60)
print('ความเร็วคือ ',v,'Km/Hr')