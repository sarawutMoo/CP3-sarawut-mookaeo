# โจทย์
# 10 + 2 = 12
# 10 - 2 = 8
# 10 * 2 = 20
# 10 / 2 = 5
num1 = int(input('เลขอะไรก็ได้ตัวแรก : '))
num2 = int(input('เลขอะไรก็ได้ตัวที่สอง : '))
#จากโจทย์ ต้องใส่เลข 10 และ 2
print(num1,'+',num2 ,'=',num1+num2)
print(num1,'-',num2 ,'=',num1-num2)
print(num1,'*',num2 ,'=',num1*num2)
print(num1,'/',num2 ,'=',int(num1/num2))